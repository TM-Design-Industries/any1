export const generateChart = (base, change, points = 20) => {
  const data = [];
  let val = base * (1 - change / 100);
  for (let i = 0; i < points; i++) {
    val += (Math.random() - 0.45) * base * 0.02;
    data.push(Math.max(val, base * 0.5));
  }
  data.push(base);
  return data;
};

